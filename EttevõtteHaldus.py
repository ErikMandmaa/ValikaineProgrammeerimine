# How many employees does the company have?

count = 1

# Data of employees

def __init__(self, name, age, position, salary):
    self.name = name
    self.age = age
    self.position = position
    self.salary = salary
    count += 1

# Profile of employees

def display_profile(self):
    print(f"Name: {self.name}")
    print(f"Age: {self.age}")
    print(f"Position: {self.position}")
    print(f"Salary: {self.salary}")

# Who is an employee?

class Employee:
    def __init__(self, name, age, position, salary):
        self.name = name
        self.age = age
        self.position = position
        self.salary = salary

# Example user: Erik Mändmaa, 18, Junior Developer, 1200 euros

employee_1 = Employee("Erik Mändmaa", 18, "Junior Developer", 1200)
display_profile(employee_1)


